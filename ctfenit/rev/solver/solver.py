flag_hex_values = [
    0x45, 0x4E, 0x49, 0x54, 0x7B, 0x48, 0x45, 0x72, 0x65, 0x5F,
    0x57, 0x33, 0x5F, 0x47, 0x6F, 0x21, 0x21, 0x21, 0x7D
]

# Convert hex values to characters
flag_chars = [chr(val) for val in flag_hex_values]

# Join characters to obtain the original flag
flag = ''.join(flag_chars)

print("The original flag is:", flag)
